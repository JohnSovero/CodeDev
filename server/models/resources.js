const { Schema, model} = require('mongoose');

const resource = new Schema({
    id: {
        type: Number,
        required: true,
        unique: true
    },
    title: {
        type: String,
        required: true
    },
    description: {
        type: String,
        required: true
    },
    content: {
        type: String,
        required: true
    }
}, { versionKey: false });

module.exports = model('resources', resource);